# Define functions for the calculator operations
def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

# Create a dictionary of operations
operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}

# Prompt the user for input and perform calculations
num1 = float(input("What is the first number? "))
while True:
    for symbol in operations:
        print(symbol)
    operation_symbol = input("Pick an operation: ")
    if operation_symbol not in operations:
        print("Invalid input. Try again.")
        continue
    num2 = float(input("What is the next number? "))
    result = operations[operation_symbol](num1, num2)
    print(f"{num1} {operation_symbol} {num2} = {result}")
    if input(f"Type 'y' to continue calculating with {result}, or 'n' to start a new calculation: ").lower() == 'y':
        num1 = result
    else:
        break
